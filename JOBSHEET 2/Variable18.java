public class Variable18 {

    public static void main(String[] args) {
        String salahSatuHobbySaya = "Saya suka bermain sepak bola dan mendaki.";
        boolean sayaSenang = true;
        char jenisKelamin = 'L';
        int umurSaya = 21;
        double ipk = 0.0;
        double tinggi = 1.76;

        System.out.println(salahSatuHobbySaya);
        System.out.println("Apakah senang? " + sayaSenang);
        System.out.println("Jenis kelamin saya: " + jenisKelamin);
        System.out.println("Umur saat ini: " + umurSaya);
        System.out.println(String.format("Saya ber-IPK %.2f, dengan tinggi badan %.2f meter", ipk, tinggi));
    }
}
